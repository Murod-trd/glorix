# ── Типы результатов ─────────────────────────────────────────────────────

@dataclass
class BenchmarkRun:
    case: TestCase
    actual_code: Optional[str]
    actual_confidence: float
    requires_clarification: bool
    clarification_message: Optional[str]
    processing_time_ms: int
    error: Optional[str] = None

    @property
    def status(self) -> str:
        if self.error:
            return "ERROR"
        if self.requires_clarification:
            return "CLARIFICATION"
        if not self.actual_code:
            return "NO_CODE"
        if self.actual_code == self.case.expected_code:
            return "EXACT_MATCH"
        if self.actual_code[:4] == self.case.expected_code[:4]:
            return "HEADING_MATCH"
        if self.actual_code[:2] == self.case.expected_code[:2]:
            return "CHAPTER_MATCH"
        return "WRONG"

    @property
    def is_correct(self) -> bool:
        return self.status in ("EXACT_MATCH", "HEADING_MATCH")

    def to_dict(self) -> dict:
        return {
            "id": self.case.id,
            "description": self.case.description[:80],
            "expected_code": self.case.expected_code,
            "expected_chapter": self.case.expected_chapter,
            "actual_code": self.actual_code,
            "actual_confidence": round(self.actual_confidence, 3),
            "status": self.status,
            "requires_clarification": self.requires_clarification,
            "clarification_message": self.clarification_message,
            "processing_time_ms": self.processing_time_ms,
            "difficulty": self.case.difficulty,
            "category": self.case.category,
            "error": self.error,
        }


@dataclass
class ConfusionEntry:
    """Запись о неправильной классификации для анализа ошибок."""
    case_id: int
    description: str
    expected_code: str
    expected_chapter: str
    actual_code: Optional[str]
    actual_chapter: Optional[str]
    category: str
    difficulty: str
    error_type: str  # wrong_code / wrong_chapter / refused / hallucination


@dataclass
class BenchmarkReport:
    runs: list[BenchmarkRun]
    total: int
    exact_matches: int
    heading_matches: int
    chapter_matches: int
    wrong: int
    clarifications: int
    errors: int
    total_time_ms: int

    # Расширенная аналитика (заполняется run_benchmark)
    category_stats: dict = field(default_factory=dict)
    confusion_entries: list = field(default_factory=list)
    chapter_confusion_matrix: dict = field(default_factory=dict)
    error_types: dict = field(default_factory=dict)

    @property
    def accuracy_exact(self) -> float:
        answered = self.total - self.clarifications - self.errors
        return self.exact_matches / answered if answered > 0 else 0.0

    @property
    def accuracy_heading(self) -> float:
        answered = self.total - self.clarifications - self.errors
        return (self.exact_matches + self.heading_matches) / answered if answered > 0 else 0.0

    @property
    def refusal_rate(self) -> float:
        return self.clarifications / self.total if self.total > 0 else 0.0

    def print_report(self) -> None:
        sep = "=" * 70
        print(sep)
        print("BENCHMARK REPORT — ТН ВЭД ЕАЭС КЛАССИФИКАТОР v3")
        print(sep)
        print(f"  Всего тестов:              {self.total}")
        print(f"  Точное совпадение (10 цифр): {self.exact_matches} ({self.accuracy_exact*100:.1f}%)")
        print(f"  Heading (4 цифры):           {self.heading_matches}")
        print(f"  Точность (10+4 цифр):       {self.accuracy_heading*100:.1f}%")
        print(f"  Неверный код:               {self.wrong}")
        print(f"  Отказ (clarification):      {self.clarifications} ({self.refusal_rate*100:.1f}%)")
        print(f"  Ошибки системы:             {self.errors}")
        print(f"  Среднее время/запрос:       {self.total_time_ms//max(self.total,1)} мс")
        print(sep)

        # По категориям
        categories: dict[str, dict] = {}
        for run in self.runs:
            cat = run.case.category
            if cat not in categories:
                categories[cat] = {"total": 0, "correct": 0, "clarification": 0}
            categories[cat]["total"] += 1
            if run.is_correct:
                categories[cat]["correct"] += 1
            if run.requires_clarification:
                categories[cat]["clarification"] += 1

        print("\nПО КАТЕГОРИЯМ:")
        for cat, stats in sorted(categories.items()):
            t = stats["total"]
            c = stats["correct"]
            r = stats["clarification"]
            acc = c / (t - r) * 100 if (t - r) > 0 else 0
            print(f"  {cat:15s}: {t:3d} тестов | {c:3d} верных ({acc:5.1f}%) | {r} отказов")

        # Ошибки
        wrong_runs = [r for r in self.runs if r.status == "WRONG"]
        if wrong_runs:
            print(f"\nОШИБКИ ({len(wrong_runs)}):")
            for r in wrong_runs[:10]:
                print(f"  [{r.case.id}] {r.case.description[:50]}")
                print(f"       Ожидалось: {r.case.expected_code}")
                print(f"       Получено:  {r.actual_code} (confidence={r.actual_confidence:.2f})")

        # Спорные случаи (ambiguous)
        ambiguous = [r for r in self.runs if r.case.difficulty in ("hard", "ambiguous")]
        if ambiguous:
            print(f"\nСПОРНЫЕ СЛУЧАИ (difficulty=hard/ambiguous): {len(ambiguous)}")
            for r in ambiguous:
                icon = "✓" if r.is_correct else ("?" if r.requires_clarification else "✗")
                print(f"  {icon} [{r.case.id}] {r.case.description[:60]}")
                if not r.is_correct and not r.requires_clarification:
                    print(f"      Ожидалось {r.case.expected_code}, получено {r.actual_code}")

        # Матрица ошибок по главам
        if self.chapter_confusion_matrix:
            print(f"\nМАТРИЦА ОШИБОК (топ-10 предсказано→ожидалось):")
            top = sorted(self.chapter_confusion_matrix.items(), key=lambda x: -x[1])[:10]
            for (pred_ch, true_ch), cnt in top:
                print(f"  гл.{pred_ch:>3s} предсказано  →  ожидалось гл.{true_ch:<3s}: {cnt} раз(а)")

        if self.error_types:
            print(f"\nТИПЫ ОШИБОК:")
            for etype, cnt in sorted(self.error_types.items(), key=lambda x: -x[1]):
                print(f"  {etype}: {cnt}")

        print(sep)

    def save_csv(self, path: str) -> None:
        with open(path, "w", newline="", encoding="utf-8") as f:
            writer = csv.DictWriter(f, fieldnames=list(self.runs[0].to_dict().keys()))
            writer.writeheader()
            writer.writerows([r.to_dict() for r in self.runs])
        print(f"CSV сохранён: {path}")

    def save_json(self, path: str) -> None:
        data = {
            "summary": {
                "total": self.total,
                "exact_matches": self.exact_matches,
                "heading_matches": self.heading_matches,
                "accuracy_exact": round(self.accuracy_exact, 4),
                "accuracy_heading": round(self.accuracy_heading, 4),
                "refusal_rate": round(self.refusal_rate, 4),
                "wrong": self.wrong,
                "errors": self.errors,
            },
            "runs": [r.to_dict() for r in self.runs],
            "chapter_confusion_matrix": {f"{k[0]}→{k[1]}": v for k, v in self.chapter_confusion_matrix.items()},
            "error_types": self.error_types,
            "category_stats": self.category_stats,
        }
        with open(path, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"JSON сохранён: {path}")


# ── Запуск ───────────────────────────────────────────────────────────────

def run_benchmark(
    cases: list[TestCase],
    model: str = "qwen2.5:7b-instruct-q4_K_M",
    dry_run: bool = False,
) -> BenchmarkReport:
    """
    Запустить benchmark.
    dry_run=True: не вызывает LLM, проверяет только инфраструктуру.
    """

    runs = []
    t_total = 0

    # Импортировать classify только если не dry-run (зависит от rank_bm25 и Qdrant)
    _classify = None
    if not dry_run:
        from rag.classifier import classify as _classify

    print(f"Запуск benchmark: {len(cases)} тестов, модель={model}, dry_run={dry_run}")
    print("-" * 70)

    for i, case in enumerate(cases):
        t0 = int(time.time() * 1000)
        try:
            if dry_run or _classify is None:
                # Имитировать ответ для проверки инфраструктуры
                run = BenchmarkRun(
                    case=case,
                    actual_code=None,
                    actual_confidence=0.0,
                    requires_clarification=True,
                    clarification_message="[dry-run]",
                    processing_time_ms=1,
                )
            else:
                result = _classify(
                    description=case.description,
                    model=model,
                    include_audit=False,
                )
                run = BenchmarkRun(
                    case=case,
                    actual_code=result.code,
                    actual_confidence=result.confidence,
                    requires_clarification=result.requires_clarification,
                    clarification_message=result.clarification_message,
                    processing_time_ms=result.processing_time_ms,
                )
        except Exception as e:
            run = BenchmarkRun(
                case=case,
                actual_code=None,
                actual_confidence=0.0,
                requires_clarification=False,
                clarification_message=None,
                processing_time_ms=int(time.time() * 1000) - t0,
                error=str(e)[:200],
            )

        t_total += run.processing_time_ms
        runs.append(run)
        icon = {"EXACT_MATCH": "✓", "HEADING_MATCH": "≈", "WRONG": "✗",
                "CLARIFICATION": "?", "ERROR": "E", "NO_CODE": "∅"}.get(run.status, "?")
        print(f"  [{i+1:3d}/{len(cases)}] {icon} {case.description[:55]}")
        if run.status == "WRONG":
            print(f"           Ожидалось: {case.expected_code} | Получено: {run.actual_code}")

    # Сводка
    exact    = sum(1 for r in runs if r.status == "EXACT_MATCH")
    heading  = sum(1 for r in runs if r.status == "HEADING_MATCH")
    chapter  = sum(1 for r in runs if r.status == "CHAPTER_MATCH")
    wrong    = sum(1 for r in runs if r.status == "WRONG")
    clarif   = sum(1 for r in runs if r.requires_clarification)
    errors   = sum(1 for r in runs if r.error)

    # Собрать confusion matrix
    confusion_entries = []
    chapter_confusion = {}
    error_type_count = {}

    for r in runs:
        if r.status in ("WRONG", "NO_CODE", "ERROR"):
            actual_ch = r.actual_code[:2] if r.actual_code and len(r.actual_code) >= 2 else None
            expected_ch = r.case.expected_chapter

            # Тип ошибки
            if r.error:
                etype = "error"
            elif r.actual_code is None:
                etype = "no_code"
            elif actual_ch != expected_ch:
                etype = "wrong_chapter"
            else:
                etype = "wrong_code"

            error_type_count[etype] = error_type_count.get(etype, 0) + 1

            entry = ConfusionEntry(
                case_id=r.case.case_id,
                description=r.case.description[:100],
                expected_code=r.case.expected_code,
                expected_chapter=expected_ch,
                actual_code=r.actual_code,
                actual_chapter=actual_ch,
                category=r.case.category,
                difficulty=r.case.difficulty,
                error_type=etype,
            )
            confusion_entries.append(entry)

            # Matrix (predicted_ch, true_ch)
            key = (actual_ch or "∅", expected_ch)
            chapter_confusion[key] = chapter_confusion.get(key, 0) + 1

    # Статистика по категориям (точность)
    cat_right, cat_total = {}, {}
    for r in runs:
        cat = r.case.category
        cat_total[cat] = cat_total.get(cat, 0) + 1
        if r.status in ("EXACT_MATCH", "HEADING_MATCH"):
            cat_right[cat] = cat_right.get(cat, 0) + 1
    category_stats = {
        cat: {"total": cat_total[cat], "correct": cat_right.get(cat, 0),
              "accuracy": round(cat_right.get(cat, 0) / cat_total[cat], 3)}
        for cat in cat_total
    }

    return BenchmarkReport(
        runs=runs, total=len(runs),
        exact_matches=exact, heading_matches=heading,
        chapter_matches=chapter, wrong=wrong,
        clarifications=clarif, errors=errors,
        total_time_ms=t_total,
        category_stats=category_stats,
        confusion_entries=confusion_entries,
        chapter_confusion_matrix=chapter_confusion,
        error_types=error_type_count,
    )


