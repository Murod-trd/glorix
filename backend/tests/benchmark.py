if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="ТН ВЭД Benchmark Runner")
    parser.add_argument("--limit",    type=int, default=200, help="Количество тестов")
    parser.add_argument("--model",    default="qwen2.5:7b-instruct-q4_K_M")
    parser.add_argument("--chapter",  default=None, help="Фильтр по главе (например: 73)")
    parser.add_argument("--category", default=None, help="Фильтр по категории")
    parser.add_argument("--dry-run",  action="store_true", help="Без LLM — только инфраструктура")
    parser.add_argument("--output",   default=None, help="Путь для CSV-вывода")
    parser.add_argument("--json",     default=None, help="Путь для JSON-вывода")
    parser.add_argument("--difficulty", default=None, help="Фильтр: easy/medium/hard/ambiguous")
    args = parser.parse_args()

    # Фильтрация
    cases = BENCHMARK_CASES
    if args.chapter:
        cases = [c for c in cases if c.expected_chapter == args.chapter]
    if args.category:
        cases = [c for c in cases if c.category == args.category]
    if args.difficulty:
        cases = [c for c in cases if c.difficulty == args.difficulty]
    cases = cases[:args.limit]

    if not cases:
        print("Нет тест-кейсов с указанными фильтрами")
        sys.exit(1)

    report = run_benchmark(cases, model=args.model, dry_run=args.dry_run)
    report.print_report()

    if args.output:
        report.save_csv(args.output)
    if args.json:
        report.save_json(args.json)
