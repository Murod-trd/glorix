/**
 * Скрининг санкционных/экспортно-контролируемых товаров — критический
 * предохранитель платформы.
 *
 * КОНТЕКСТ: ранее ни в Marketplace.jsx (размещение товара), ни в
 * CreateTender.jsx (создание тендера) не было НИКАКОЙ проверки введённого
 * текста на принадлежность к санкционным/экспортно-контролируемым
 * категориям. Поле `sanctions: false` и `aiCheck.sanctionsOk: true` в
 * marketplace.js — статичные демо-данные, не связанные с реальным вводом
 * пользователя. Основатель платформы лично протестировал это, создав
 * тендер и товар с явно санкционным описанием — ни блокировки, ни
 * предупреждения не последовало. Если так останется в реальном продукте,
 * платформа сама создаёт юридический риск попадания под санкции за
 * фасилитацию запрещённой торговли.
 *
 * ЧТО ЭТО РЕШАЕТ И ЧТО НЕ РЕШАЕТ:
 * Настоящая классификация экспортного контроля (ECCN, EU Dual-Use Annex I,
 * списки Wassenaar/MTCR/NSG/Australia Group) требует точного товарного кода
 * и реальной базы данных — keyword-matching по названию товара НЕ может
 * заменить это и не должен claim'иться как замена. Этот модуль даёт два
 * уровня защиты, оба явно и честно обозначенные:
 *
 * 1. HARD BLOCK — однозначные категории (вооружение, военная техника,
 *    ядерные/химические/биологические материалы), которые запрещены к
 *    гражданской B2B-торговле во всех основных режимах контроля без
 *    исключений. Публикация блокируется полностью, без возможности продолжить.
 *
 * 2. REVIEW REQUIRED — категории двойного назначения (микроэлектроника,
 *    телеком-оборудование, отдельные химикаты, металлы спецификаций военного
 *    назначения), которые МОГУТ требовать экспортной лицензии в зависимости
 *    от точной спецификации и страны назначения. Публикация блокируется до
 *    подтверждения пользователем, что товар проверен вручную — платформа
 *    не утверждает, что товар "чист", только что он требует проверки.
 *
 * Источник категоризации: общедоступная структура категорий контроля
 * двойного назначения (Wassenaar Arrangement / EU Regulation 2021/821
 * Annex I, категории 0-9) и санкционные товарные группы, регулярно
 * фигурирующие в режимах OFAC/EU/UN в отношении торговли с/через регион СНГ
 * (вооружение, авиационные и аэрокосмические компоненты, нефть и
 * нефтепродукты определённых классов, отдельные металлы).
 */

const HARD_BLOCK_KEYWORDS = [
  // Вооружение и военная техника
  'оружие', 'оружия', 'оружием', 'вооружение', 'вооружения',
  'боеприпас', 'боеприпасы', 'патрон', 'патроны', 'снаряд', 'снаряды',
  'автомат', 'пистолет', 'пулемёт', 'пулемет', 'винтовка', 'гранат',
  'танк', 'бронетехника', 'ракета', 'ракеты', 'военн', 'milитар',
  'weapon', 'ammunition', 'firearm', 'missile', 'warhead', 'explosive',
  'взрывчат', 'взрывчатк', 'детонатор', 'мина противопехотная',
  // Ядерные, химические, биологические материалы
  'ядерн', 'плутоний', 'уран обогащённый', 'уран обогащенный',
  'радиоактивн', 'обогащение урана', 'химическое оружие',
  'биологическое оружие', 'нервно-паралитич', 'боевые отравляющие',
  'nuclear weapon', 'chemical weapon', 'biological weapon', 'nerve agent',
];

const REVIEW_REQUIRED_KEYWORDS = [
  // Двойного назначения — электроника/телеком/навигация (Wassenaar Cat. 3-7)
  'шифрование', 'криптограф', 'радар', 'лазер военного', 'гироскоп',
  'навигационная система военного', 'беспилотник', 'дрон военного',
  'квантовый компьютер', 'квантовые вычисления',
  'encryption hardware', 'military-grade', 'dual-use',
  // Авиация/аэрокосмос
  'авиационный двигатель', 'ракетный двигатель', 'космический аппарат',
  'aerospace component', 'rocket engine',
  // Нефть/нефтепродукты повышенного контроля (часто фигурируют в санкционных режимах против РФ)
  'нефтепродукт', 'дизельное топливо экспорт', 'сырая нефть',
  'crude oil export', 'refined petroleum',
  // Металлы и сплавы военной спецификации
  'титановый сплав авиационн', 'специальная сталь военного назначения',
];

function normalize(text) {
  return (text || '').toLowerCase();
}

/**
 * Проверяет текст (название товара + опционально категорию/спецификации)
 * на признаки санкционных/экспортно-контролируемых категорий.
 *
 * @returns {{ status: 'clear'|'review_required'|'blocked', matchedTerms: string[] }}
 */
export function screenForSanctions(...textParts) {
  const combined = normalize(textParts.join(' '));

  const blockMatches = HARD_BLOCK_KEYWORDS.filter(kw => combined.includes(kw));
  if (blockMatches.length > 0) {
    return { status: 'blocked', matchedTerms: blockMatches };
  }

  const reviewMatches = REVIEW_REQUIRED_KEYWORDS.filter(kw => combined.includes(kw));
  if (reviewMatches.length > 0) {
    return { status: 'review_required', matchedTerms: reviewMatches };
  }

  return { status: 'clear', matchedTerms: [] };
}
