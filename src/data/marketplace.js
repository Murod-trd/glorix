export function calcMarketplaceFee(amount) {
  if (amount <= 5000) return 1.5;
  if (amount >= 50000) return 0.5;
  return +(1.5 - (1.5 - 0.5) * ((amount - 5000) / (50000 - 5000))).toFixed(2);
}
